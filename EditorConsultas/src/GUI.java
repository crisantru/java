    private void btn_consultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_consultarActionPerformed
        if(validarDatosConexion()){
            String host = this.txt_host.getText();
            String user = this.txt_usuario.getText();
            String pass = this.pass_contrasena.getText();
            String bd = this.txt_basedatos.getText();
            Integer puerto = Integer.parseInt(this.spn_puerto.getValue().toString());
            
            DefaultTableModel model = new DefaultTableModel();
            ConexionBD conn = null;
            try{
                conn = new ConexionBD(host, puerto, bd, user, pass);
                String consulta = this.txa_consulta.getText();
                if(soloConsulta(consulta)){
                    ResultSet rs = conn.query(consulta).getResultSet();
                    int colums = 0;
                    if(rs != null){
                        //--------------------LEER NOMBRE DE COLUMNAS---------------------
                        colums = rs.getMetaData().getColumnCount();
                        String NOMBRE_COLUMNAS[] = new String[colums];
                        for(int i = 0; i < colums; i++){
                            NOMBRE_COLUMNAS[i] = rs.getMetaData().getColumnName(i + 1);
                        }
                        //--------------------CREAR MODELO TABLA------------------------
                        model = new DefaultTableModel(new Object[0][0], NOMBRE_COLUMNAS);
                        //--------------------LEER DATOS DE LA CONSULTA-----------------
                        while(rs.next()){
                            Object fila[] = new Object[colums];
                            for(int i = 0; i < colums; i++){
                                fila[i] = rs.getString(i + 1);
                            }
                            model.addRow(fila);
                        }
                    }
                }else{
                    int filasafectadas = conn.update(consulta).getUpdateCount();
                    JOptionPane.showMessageDialog(this, filasafectadas+"fila(s) afectada(s)", "comando ejecutado", JOptionPane.INFORMATION_MESSAGE);
                }
            }catch(Exception ex){
                JOptionPane.showMessageDialog(this,ex.getMessage(), "Error", JOptionPane.WARNING_MESSAGE);
            }finally{
                if(conn!= null){
                        conn.close();
                }

            }
            this.tbl_datos.setModel(model);
        }
    }//GEN-LAST:event_btn_consultarActionPerformed

