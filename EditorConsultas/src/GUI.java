    public boolean soloConsulta(String consulta){
        String comandosActualizacion [] = {"INSERT", "UPDATE", "DELETE", "CREATE", "ALTER", "DROP", "TRUNCATE"};
        if(consulta != null){
            consulta = consulta.toUpperCase();
            for(String com : comandosActualizacion){
                if(consulta.contains(com)){
                    return false;
                }
            }
        }
        return true;
    }
    
    public boolean validarDatosConexion(){
        if(this.txt_host == null || txt_host.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "El campo hostname es requerido",
                    "campo vacio", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        if(this.txt_basedatos == null || txt_basedatos.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "El campo SID es requerido",
                    "campo vacio", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        if(this.txt_usuario == null || txt_usuario.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "El campo Usuario es requerido", 
                    "campo vacio", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        if(this.pass_contrasena == null || pass_contrasena.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "El campo Contraseñaes requerido", 
                    "campo vacio", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        if(this.spn_puerto == null || Integer.parseInt(spn_puerto.getValue().toString()) <= 0){
            JOptionPane.showMessageDialog(this, "El campo puerto es requerido y "
                    + "debe ser mayor a cero", "Campo vacio o incorrecto",
                    JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        return true;
    }
    
    
